UPDATE persons
SET
    gender = $1,
    last_name = $2,
    last_name_prefix = $3,
    first_name = $4,
    initials = $5,
    date_of_birth = $6,
    bsn = $7,
    no_bsn_confirmed = $8,
    place_of_residence = $9,
    country_of_residence = $10,
